db.Employee.aggregate([
    // $denseRank assigns ranking within each department
    // partitionBy: departmentId → resets rank for each department
    // sortBy: salary DESC → highest salary gets rank 1
    {
        $setWindowFields: {
            partitionBy: "$departmentId",
            sortBy: { salary: -1 },
            output: {
                rank: {
                    $denseRank: {}
                    }
                }
            }
        },
    //    +------------------------+------------+--+-----+----+------+
    //    |_id                     |departmentId|id|name |rank|salary|
    //    +------------------------+------------+--+-----+----+------+
    //    |69d236428d8b8c203979973a|1           |4 |Max  |1   |90000 |
    //    |69d236428d8b8c2039799737|1           |1 |Joe  |2   |85000 |
    //    |69d236428d8b8c203979973c|1           |6 |Randy|2   |85000 |
    //    |69d236428d8b8c203979973d|1           |7 |Will |3   |70000 |
    //    |69d236428d8b8c203979973b|1           |5 |Janet|4   |69000 |
    //    |69d236428d8b8c2039799738|2           |2 |Henry|1   |80000 |
    //    |69d236428d8b8c2039799739|2           |3 |Sam  |2   |60000 |
    //    +------------------------+------------+--+-----+----+------+

    // LOOKUP Department Table
    {
        $lookup: {
            from: 'Department',
            localField: 'departmentId',
            foreignField: 'id',
            as: 'department_info'
            }
        },
    { $unwind: '$department_info' },
    //    <- LOOKUP_UNWIND_OUTPUT ->
    //    +------------------------+------------+-----------------------------------------------------------------------+--+-----+----+------+
    //    |_id                     |departmentId|department_info                                                        |id|name |rank|salary|
    //    +------------------------+------------+-----------------------------------------------------------------------+--+-----+----+------+
    //    |69d236428d8b8c203979973a|1           |{"_id": {"$oid": "69d236418d8b8c2039799735"}, "id": 1, "name": "IT"}   |4 |Max  |1   |90000 |
    //    |69d236428d8b8c2039799737|1           |{"_id": {"$oid": "69d236418d8b8c2039799735"}, "id": 1, "name": "IT"}   |1 |Joe  |2   |85000 |
    //    |69d236428d8b8c203979973c|1           |{"_id": {"$oid": "69d236418d8b8c2039799735"}, "id": 1, "name": "IT"}   |6 |Randy|2   |85000 |
    //    |69d236428d8b8c203979973d|1           |{"_id": {"$oid": "69d236418d8b8c2039799735"}, "id": 1, "name": "IT"}   |7 |Will |3   |70000 |
    //    |69d236428d8b8c203979973b|1           |{"_id": {"$oid": "69d236418d8b8c2039799735"}, "id": 1, "name": "IT"}   |5 |Janet|4   |69000 |
    //    |69d236428d8b8c2039799738|2           |{"_id": {"$oid": "69d236418d8b8c2039799736"}, "id": 2, "name": "Sales"}|2 |Henry|1   |80000 |
    //    |69d236428d8b8c2039799739|2           |{"_id": {"$oid": "69d236418d8b8c2039799736"}, "id": 2, "name": "Sales"}|3 |Sam  |2   |60000 |
    //    +------------------------+------------+-----------------------------------------------------------------------+--+-----+----+------+

    // MATCH rank <= 3
    {
        $match: {
            rank: { $lte: 3 }
            }
        },
    //    <- MATCH_OUTPUT ->
    //    +------------------------+------------+-----------------------------------------------------------------------+--+-----+----+------+
    //    |_id                     |departmentId|department_info                                                        |id|name |rank|salary|
    //    +------------------------+------------+-----------------------------------------------------------------------+--+-----+----+------+
    //    |69d236428d8b8c203979973a|1           |{"_id": {"$oid": "69d236418d8b8c2039799735"}, "id": 1, "name": "IT"}   |4 |Max  |1   |90000 |
    //    |69d236428d8b8c2039799737|1           |{"_id": {"$oid": "69d236418d8b8c2039799735"}, "id": 1, "name": "IT"}   |1 |Joe  |2   |85000 |
    //    |69d236428d8b8c203979973c|1           |{"_id": {"$oid": "69d236418d8b8c2039799735"}, "id": 1, "name": "IT"}   |6 |Randy|2   |85000 |
    //    |69d236428d8b8c203979973d|1           |{"_id": {"$oid": "69d236418d8b8c2039799735"}, "id": 1, "name": "IT"}   |7 |Will |3   |70000 |
    //    |69d236428d8b8c2039799738|2           |{"_id": {"$oid": "69d236418d8b8c2039799736"}, "id": 2, "name": "Sales"}|2 |Henry|1   |80000 |
    //    |69d236428d8b8c2039799739|2           |{"_id": {"$oid": "69d236418d8b8c2039799736"}, "id": 2, "name": "Sales"}|3 |Sam  |2   |60000 |
    //    +------------------------+------------+-----------------------------------------------------------------------+--+-----+----+------+

    // Project Department, Employee, Salary
    {
        $project: {
            _id: 0,
            Department: '$department_info.name',
            Employee: '$name',
            Salary: '$salary'
            }
        }
    //    <- FINAL_OUTPUT ->
    //    +----------+--------+------+
    //    |Department|Employee|Salary|
    //    +----------+--------+------+
    //    |IT        |Max     |90000 |
    //    |IT        |Joe     |85000 |
    //    |IT        |Randy   |85000 |
    //    |IT        |Will    |70000 |
    //    |Sales     |Henry   |80000 |
    //    |Sales     |Sam     |60000 |
    //    +----------+--------+------+

    ])
//    .explain("executionStats");